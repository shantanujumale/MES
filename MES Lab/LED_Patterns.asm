ORG 0000H
START:
	MOV P1,#0FFH
	ACALL DELAY
PATTERN1:
	MOV P1,#55H
	ACALL DELAY
	MOV P1,#0AAH
	ACALL DELAY
	SJMP PATTERN2
PATTERN2:
	MOV A,#01H
SHIFT_LEFT:
	MOV P1,A
	ACALL DELAY
	RL A
	CJNE A,#00H, SHIFT_LEFT
	SJMP PATTERN3
PATTERN3:
	MOV A,#80H
SHIFT_RIGHT:
	MOV P1,A
	ACALL DELAY
	RR A
	CJNE A,#00H,SHIFT_RIGHT
	SJMP PATTERN4
PATTERN4:
	MOV P1,#0FFH
	ACALL DELAY
	MOV P1,#00H
	ACALL DELAY
	SJMP START
DELAY:
	MOV R6,#5
	D2: DJNZ R6,D2
	RET
END